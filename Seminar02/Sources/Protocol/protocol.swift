import UIKit

/*
 델리게이트 패턴 5단계 설명:

 1️⃣ Protocol 정의: 깃허브미미나 - 해야 할 일들을 정의
 2️⃣ iOS 파트장 클래스: weak var 파트원: 미미나? - delegate를 가지고 있음(대리자=파트원)
 3️⃣ 파트원 클래스가 Protocol 채택: class 파트원: 미미나 - 실제 일을 처리
 4️⃣ init에서 이명진.파트원 = self: 이명진(iOS파트장)이 메소드 호출하면 → 선영주(파트원)가 실제로 실행
 5️⃣ 이명진(iOS파트장)의 깃허브미미나() 호출 -> 파트원?.깃허브미미나실제로진행하기() → 선영주(파트원)의 메소드가 실행됨
 */

// 1️⃣ Protocol 정의 - 해야 할 일 정의
protocol 미미나: AnyObject {
    func 깃허브미미나실제로진행하기()
}

protocol iOS파트장세미나내용2주차 {
    func 데이터전달()
    func 프로토콜()
    func 델리게이트패턴()
    func 익스텐션()
    func 오토레이아웃()
}

// 2️⃣ 퍄트장 클래스 - 파트원 속성을 가지고 있음
class iOS파트장: iOS파트장세미나내용2주차 {

    weak var 미미나_대신_해줄_파트원: 미미나?
    // 깃허브 미미나를 할 수 있는 파트원을 가지고 있습니다.
    // (옵셔널 이기 때문에 제가 파트원 에게 의존을 하진 않습니다.)

    func 데이터전달() {
        print("데이터 전달은 크게 2가지로 나눠집니다 ... 블라블라")
    }

    func 프로토콜() {
        print("추상화 .. 프로토콜 확장 너무 중요해요")
    }

    func 델리게이트패턴() {
        print("짬 때리기 라는겁니다")
    }

    func 익스텐션() {
        print("익스텐션이 그냥 코드 추가하는 것만 알면 안됩니다.")
    }

    func 오토레이아웃() {
        print("SnapKit 할까? ㅋㅋ")
    }

    func 깃허브미미나() {
        print("깃허브를 영주가 해준다고?!?!? ")
        // 5️⃣ delegate 메소드 호출 - 실제로는 파트원이 실행
        미미나_대신_해줄_파트원?.깃허브미미나실제로진행하기()
    }

    func 세미나() {
        print("=== 이명진 파트장의 세미나 시작! ===\n")

        print("1. 데이터 전달")
        데이터전달()

        print("\n2. 프로토콜")
        프로토콜()

        print("\n3. 델리게이트 패턴")
        델리게이트패턴()

        print("\n4. 익스텐션")
        익스텐션()

        print("\n5. 오토레이아웃")
        오토레이아웃()

        print("\n6. 깃허브 미미나 (파트원에게 위임)")
        깃허브미미나()
    }
}

// 3️⃣ 파트원 클래스 - Protocol 채택, 실제 일을 처리
class 파트원: 미미나 {

    var 이명진: iOS파트장

    init(이명진: iOS파트장) {
        self.이명진 = 이명진
        
        // 4️⃣ 이명진.미미나_대신_해줄_파트원 = self 설정
        이명진.미미나_대신_해줄_파트원 = self
    }

    func 깃허브미미나실제로진행하기() {
        print("네! 제가 깃허브 미미나 진행하겠습니다!")
        print("YB들~ git add, git commit, git push 순서대로 하면 돼요! ㅎㅎ")
        print("궁금한 거 있으면 언제든 물어보세요~")
    }
}
